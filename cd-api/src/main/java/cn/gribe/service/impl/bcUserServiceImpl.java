package cn.gribe.service.impl;


import cn.gribe.common.validator.Assert;
import cn.gribe.dao.UserDao;
import cn.gribe.entity.BcUserEntity;
import cn.gribe.entity.TokenEntity;
import cn.gribe.form.LoginForm;
import cn.gribe.service.bcUserService;
import com.baomidou.mybatisplus.service.impl.ServiceImpl;
import cn.gribe.common.exception.RRException;
import cn.gribe.service.TokenService;
import org.apache.commons.codec.digest.DigestUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.HashMap;
import java.util.Map;

@Service("userService")
public class bcUserServiceImpl extends ServiceImpl<UserDao, BcUserEntity> implements bcUserService {
	@Autowired
	private TokenService tokenService;

	@Override
	public BcUserEntity queryByMobile(String mobile) {
		BcUserEntity userEntity = new BcUserEntity();
		userEntity.setMobile(mobile);
		return baseMapper.selectOne(userEntity);
	}

	@Override
	public Map<String, Object> login(LoginForm form) {
		BcUserEntity user = queryByMobile(form.getMobile());
		Assert.isNull(user, "手机号或密码错误");

		//密码错误
		if(!user.getPassword().equals(DigestUtils.sha256Hex(form.getPassword()))){
			throw new RRException("手机号或密码错误");
		}

		//获取登录token
		TokenEntity tokenEntity = tokenService.createToken(user.getUserId());

		Map<String, Object> map = new HashMap<>(2);
		map.put("token", tokenEntity.getToken());
		map.put("expire", tokenEntity.getExpireTime().getTime() - System.currentTimeMillis());

		return map;
	}

}
